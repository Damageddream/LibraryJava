package exercises.arrays;

public class Person {
    private int weight;
    private int id;


    public Person(int weight, int id) {
        this.weight = weight;
        this.id = id;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
