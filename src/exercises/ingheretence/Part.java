package exercises.ingheretence;

public class Part {
    private int id;
    private String producentName;
    private String model;
    private int seriesNr;



    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProducentName() {
        return producentName;
    }

    public void setProducentName(String producentName) {
        this.producentName = producentName;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getSeriesNr() {
        return seriesNr;
    }

    public void setSeriesNr(int seriesNr) {
        this.seriesNr = seriesNr;
    }
}
